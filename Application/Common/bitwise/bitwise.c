bool isDigitString( const char *buffer, uint16_t len )
{
  for( uint16_t index = 0; index < len; index++ )
  {
    if( ( ( buffer[ index ] < '0' ) && ( buffer[ index ] > '9' ) ) && ( buffer[ index ] != '.' ) )
    {
      return false;
    }
  }

  return true;
}
